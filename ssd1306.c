// Send command helper function.
static void ssd1306_send_command(uint8_t cmd) {
    uint8_t data[2] = {0x00, cmd};
    i2c_write_blocking(ssd1306_i2c, ssd1306_address, data, 2, false);
}

// Display initialization function.
void ssd1306_init(i2c_inst_t *i2c, uint8_t sda_pin, uint8_t scl_pin, uint8_t addr) {
    ssd1306_i2c = i2c;
    ssd1306_address = addr;

    // Set up SDA and SCL
    i2c_init(i2c, 400 * 1000); // 400kHz I2C
    gpio_set_function(sda_pin, GPIO_FUNC_I2C);
    gpio_set_function(scl_pin, GPIO_FUNC_I2C);
    gpio_pull_up(sda_pin);
    gpio_pull_up(scl_pin);

    // Initialization flow
    ssd1306_send_command(0xA8); // Initiate set MUX ratio
    ssd1306_send_command(0x3F); // 64 pixel height (COM0-COM63)
    ssd1306_send_command(0xD3); // Initiate set display offset
    ssd1306_send_command(0x00); // Don't offset display
    ssd1306_send_command(0x40); // Set display start line to the top
    ssd1306_send_command(0xA1); // Set segment re-map (column address 0 is mapped to SEG0) (left-right)
    ssd1306_send_command(0xC8); // Set COM output scan direction to COM63-COM0 (top-bottom) ok i think its actually bottom-top but idk this is confusing,.
    ssd1306_send_command(0xDA); // Set COM pins hardware configuration
    ssd1306_send_command(0x02); // Sequential COM pin configuration (not alternating)
    ssd1306_send_command(0x81); // Initiate set contrast control for BANK0
    ssd1306_send_command(0x7F); // Set to mid-brightness
    ssd1306_send_command(0xA4); // Disable having entire display on
    ssd1306_send_command(0xA6); // Set display to normal mode (A7 is "inverted", think light vs. dark mode)
    ssd1306_send_command(0xD5); // Display clock divide ratio; need to investigate this
    ssd1306_send_command(0x80); // Set oscillator frequency Fosc; need to investigate this
    ssd1306_send_command(0x8D); // Initiate enable charge pump regulator - literally what even is this
    ssd1306_send_command(0x14); // Enable charge pump
    ssd1306_send_command(0xAF); // Turn display on (AE for sleep mode)
}

void ssd1306_clear(void) {
    memset(buffer, 0, sizeof(buffer)); // Set buffer to all 0s
    ssd1306_update();
}

void ssd1306_update(void) {
    for (uint8_t page = 0; page < (SSD1306_HEIGHT / 8); page++) {
        ssd1306_send_command(0xB0 + page); // Page start address
        ssd1306_send_command(0x00); // Low column start address
        ssd1306_send_command(0x10); // High column start address

        uint8_t data[SSD1306_WIDTH + 1];
        data[0] = 0x40;
        memcpy(&data[1], &buffer[page * SSD1306_WIDTH], SSD1306_WIDTH);

        i2c_write_blocking(ssd1306_i2c, ssd1306_address, data, SSD1306_WIDTH + 1, false);
    }
}

void ssd1306_blink(uint16_t loops, uint16_t ms) {
    for (uint16_t i = 0; i < loops; i++) {
        ssd1306_send_command(0xA5);
        sleep_ms(ms);
        ssd1306_clear();
        ssd1306_send_command(0xA4);
        sleep_ms(ms);
    }
}

void ssd1306_write_text(uint8_t textBuff[], uint8_t row, uint8_t column) {
    if (!textBuff) return;

    uint8_t x0 = column * CHAR_PITCH;
    uint8_t y0 = row * 8;

    if (y0 >= SSD1306_HEIGHT) return;

    for (uint8_t i = 0; textBuff[i] != '\0'; ++i) {
        uint8_t c = textBuff[i];

        if (x0 >= SSD1306_WIDTH) break;

        const uint8_t *glyph = font5x7[c - 0x20];

        for (uint8_t col = 0; col < FONT_W; ++col) {
            uint8_t px = x0 + col;
            
            uint8_t page = y0 >> 3;         // y0 / 8
            uint8_t bit_offset = y0 & 0x07; // y0 % 8

            uint8_t col_bits = glyph[col] & 0x7F; // mask to last 7 bits
            uint8_t low = (col_bits << bit_offset);
            uint8_t high = (bit_offset == 0) ? 0 : col_bits >> (8 - bit_offset);

            uint8_t glyph_mask = (1u << FONT_H) - 1u;
            uint8_t low_mask = glyph_mask << bit_offset;
            uint8_t high_mask = (bit_offset == 0) ? 0 : glyph_mask >> (8 - bit_offset);

            if (page >= 0 && page < (SSD1306_HEIGHT / 8)) {
                uint8_t idx_low = page * SSD1306_WIDTH + px;
                buffer[idx_low] = (buffer[idx_low] & ~low_mask) | (low & low_mask);
            }

            if (bit_offset != 0) {
                uint8_t next_page = page + 1;
                if (next_page >= 0 && next_page < (SSD1306_HEIGHT / 8)) {
                    uint8_t idx_high = next_page * SSD1306_WIDTH + px;
                    buffer[idx_high] = (buffer[idx_high] & ~high_mask) | (high & high_mask);
                }
            }
        }

        uint8_t space_x = x0 + FONT_W;
        
        if (space_x >= 0 && space_x < SSD1306_WIDTH) {
            uint8_t page = y0 >> 3; // y0 / 8
            uint8_t bit_offset = y0 & 0x07;
            uint8_t glyph_mask = (1u << FONT_H) - 1u;
            uint8_t low_mask = glyph_mask << bit_offset;
            if (page >= 0 && page < (SSD1306_HEIGHT / 8)) {
                uint8_t idx_low = page * SSD1306_WIDTH + space_x;
                buffer[idx_low] &= ~low_mask;
            }
            if (bit_offset != 0) {
                uint8_t next_page = page + 1;
                if (next_page >= 0 && next_page < (SSD1306_HEIGHT / 8)) {
                    uint8_t high_mask = glyph_mask >> (8 - bit_offset);
                    uint8_t idx_high = next_page * SSD1306_WIDTH + space_x;
                    buffer[idx_high] &= ~high_mask;
                }
            }
        }

        x0 += CHAR_PITCH;
    }
}